public class nestedif {
    public static void main(String[] args) {
        int time = 24;
        if(time <= 12 ){
            System.out.println("good morning");
        }
        else if(time <= 16){
            System.out.println("good afternoon");
        }

        else if(time <=20 ){
            System.out.println("good evening");
        }
        else {
            System.out.println("good night");
        }

    }
    
}
